class Point:
    def __init__(self, xc=0, yc=0, zc=0):
        self.xc = xc
        self.yc = yc
        self.zc = zc  
               
    def setXc(xc):
        self.xc = xc
    
    def setYc(yc):
        self.yc = yc
        
    def setZc(zc):
        self.zc = zc
        
    def getXc(self):
        return self.xc
    
    def getYc(self):
        return self.yc
        
    def getZc(self):
        return self.zc